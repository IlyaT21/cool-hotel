package beans;

/**
 *
 * @author Ilija
 */
public class Hotel {

    private int id;
    protected String naziv;
    protected int user_id;

    public Hotel() {

    }

    public Hotel(int id, String naziv, int user_id) {
        this.id = id;
        this.naziv = naziv;
        this.user_id = user_id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }
}
